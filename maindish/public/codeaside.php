6. Cover the bowl and refrigerate the chocolate mixture for at least 2 hours, or until firm.
7. Once the chocolate mixture is firm, use a small spoon or a melon baller to scoop out portions and roll them into balls using your hands.
8. Roll the chocolate truffles in your desired coatings, such as cocoa powder, powdered sugar, chopped nuts, shredded coconut, or drizzle them with melted chocolate.
9. Place the finished truffles on a baking sheet lined with parchment paper and refrigerate until firm.
